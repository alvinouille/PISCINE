int	add(int a, int b)
{
	return (a + b);
}

int	sous(int a, int b)
{
	return (a - b);
}

int	mult(int a, int b)
{
	return (a * b);
}

int	div(int a, int b)
{
	return (a / b);
}

int	modulo(int a, int b)
{
	return (a % b);
}
