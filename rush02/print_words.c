#include <unistd.h>

void	print_words(char *str)
{
	int	i;

	i = 0;
	while (str[i] != ':')
		i++;
	while (str[i] == ' ')
		i++;
	while (str[i] > 32 && str[i] < 127 && (str[i]))
	{
		write(1, &str[i], 1);
		i++;
	}
}
