void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tmp;

	tmp = 0;
	j = size -1;
	i = 0;
	while (i < (size / 2))
	{
		(tmp) = (tab[i]);
		(tab[i]) = (tab[j]);
		(tab[j]) = (tmp);
		i++;
		j--;
	}
}
